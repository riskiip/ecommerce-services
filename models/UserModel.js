const mongoose = require('mongoose');

/* Create schema first */
const userSchema = mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    mobile: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
});

/* Then export model using schema that we design earlier */
module.exports = mongoose.model("User", userSchema);